    @PersistenceContext
    private EntityManager em;
    @Override
    @Transactional
    public Meal save(Meal meal, int userId) {
        if (meal.isNew()){
            User user = new User();
            user.setId(userId);
            meal.setUser(user);
            em.persist(meal);
        } else {
            em.merge(meal);
        }
        return meal;
    }

